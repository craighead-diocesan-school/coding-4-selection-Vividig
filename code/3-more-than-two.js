function moreThanTwo() {
  // write your code here
  let num = prompt('What is your number?')

  if (num < 2) {
    alert("Your number is less than 2")
  } 
  if (num > 2) {
    alert("Your number is greater than 2")
  } 
  if (num == 2) {
    alert("Your number equals to 2")
  } 

  alert('Goodbye')
}
